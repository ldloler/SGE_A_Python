# Variable del dia de la setmana
dia_setmana = "divendres"

# Es dilluns?
if (dia_setmana == "dilluns"):
    print("Avui es dilluns")
# Es dimarts?
elif (dia_setmana == "dimarts"):
    print("Avui es dimarts")
# Es dimecres?
elif (dia_setmana == "dimecres"):
    print("Avui es dimecres")
# Es dijous?
elif (dia_setmana == "dijous"):
    print("Avui es dijous")
# Es divendres?
elif (dia_setmana == "divendres"):
    print("Avui es divendres")
# Es dissabte?
elif (dia_setmana == "dissabte"):
    print("Avui es dissabte")
# Es diumenge?
elif (dia_setmana == "diumenge"):
    print("Avui es diumenge")
# Es alguna altre cosa? Es un error.
else:
    print("Error,", dia_setmana, "no es un dia correcte")